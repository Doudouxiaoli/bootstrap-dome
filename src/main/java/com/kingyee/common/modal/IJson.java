package com.kingyee.common.modal;

/**
 * json常量接口
 */
public interface IJson {

    /**
     * 成功失败
     */
    String SUCCESS = "success";

    /**
     * 消息
     */
    String MSG = "msg";

    /**
     * 返回数据
     */
    String DATA = "data";

    /**
     * 总数
     */
    String COUNT = "count";

    /**
     * 总页数
     */
    String PAGES = "pages";

    /**
     * 当前页
     */
    String PAGE = "page";

    /**
     * 每页条数
     */
    String SIZE = "size";

    String JSON = "org/json";

    /**
     * 成功失败
     */
    String CODE = "code";

}
